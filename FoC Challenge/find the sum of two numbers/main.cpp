#include <iostream>

using namespace std;

int main()
{
    int n1;
    int n2;
    int add;
    
    //Input:
    cout << "Input the first number: ";
    cin >> n1;
    
    cout << "Input the second number: ";
    cin >> n2;
    
    //Processing:
    add = n1 + n2;
    
    //Output:
    cout << "The sum of " << n1 << " and " << n2 << " is " << add;
    

    return 0;
}
